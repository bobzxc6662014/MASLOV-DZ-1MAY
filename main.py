import sqlite3

connection = sqlite3.connect('nevz.db')
cursor = connection.cursor()

cursor.execute("PRAGMA foreign_keys = ON")

# Удаляем таблицы, если они существуют
cursor.execute("DROP TABLE IF EXISTS borrowings")
cursor.execute("DROP TABLE IF EXISTS workers")
cursor.execute("DROP TABLE IF EXISTS bikes")
cursor.execute("DROP TABLE IF EXISTS magazin")  # Исправил название таблицы на 'magazin'

# Создаём таблицы
cursor.execute("""
    CREATE TABLE workers (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT NOT NULL,
        post TEXT NOT NULL,
        department TEXT NOT NULL,
        work_experience INTEGER NOT NULL,
        salary INTEGER NOT NULL
    )
""")

cursor.execute("""
    CREATE TABLE bikes (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        series TEXT NOT NULL,
        type TEXT NOT NULL,
        current_type TEXT NOT NULL,
        cost INTEGER NOT NULL,
        prodano INTEGER NOT NULL
    )
""")

cursor.execute("""
    CREATE TABLE magazin (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        nomer TEXT NOT NULL,
        country TEXT NOT NULL,
        city TEXT NOT NULL
    )
""")

cursor.execute("""
    CREATE TABLE borrowings (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        magazin_id INTEGER NOT NULL,
        bikes_id INTEGER NOT NULL,
        date_borrowed TEXT NOT NULL,
        date_returned TEXT,
        FOREIGN KEY (magazin_id) REFERENCES magazin(id),
        FOREIGN KEY (bikes_id) REFERENCES bikes(id)
    )
""")

